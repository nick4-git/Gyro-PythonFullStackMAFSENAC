/////// Exemplos de Dados em JavaScript ///////

let numeroA = 10; // int
let numeroB = 20;
let numeroC = 30;

let soma = numeroA + numeroB + numeroC;
console.log("A soma dos números é: " + soma); // string + int

let subtracao = numeroC - numeroB;
console.log("A subtração dos números é: " + subtracao);

let multiplicacao = numeroA * numeroB;
console.log("A multiplicação dos números é: " + multiplicacao);

let divisao = numeroC / numeroA;
console.log("A divisão dos números é: " + divisao);